func writeStarship(t *Theme) error {
	content := fmt.Sprintf(`# WinShell theme: %s
# Generated by winshell — do not edit manually

palette = "%s"

[palettes.%s]
%s

[character]
success_symbol = "[❯](bold fg:%s)"
error_symbol = "[❯](bold red)"

[directory]
style = "bold fg:%s"
truncation_length = 3
truncate_to_repo = true

[git_branch]
symbol = " "
style = "bold fg:%s"

[git_status]
style = "fg:%s"

[nodejs]
symbol = " "
style = "bold fg:%s"

[python]
symbol = " "
style = "bold fg:%s"

[golang]
symbol = " "
style = "bold fg:%s"
`, t.Name, t.Name, t.Name, t.Starship.Palette,
		t.Starship.PromptColor,
		t.Starship.DirColor,
		t.Starship.BranchColor,
		t.Starship.StatusColor,
		t.Starship.NodeColor,
		t.Starship.PythonColor,
		t.Starship.GoColor,
	)

	path := config.StarshipPath()
	if err := os.MkdirAll(filepath.Dir(path), 0o755); err != nil {
		return err
	}
	return os.WriteFile(path, []byte(content), 0o644)
}

func writeFzfColors(t *Theme) error {
	// Read current profile
	profilePath := config.PowerShellProfilePath()
	data, err := os.ReadFile(profilePath)
	if err != nil {
		return fmt.Errorf("could not read profile: %w", err)
	}

	// Replace FZF_DEFAULT_OPTS block
	lines := strings.Split(string(data), "\n")
	newLines := []string{}
	skip := false

	for _, line := range lines {
		if strings.Contains(line, "$env:FZF_DEFAULT_OPTS") {
			skip = true
		}
		if skip && strings.Contains(line, "--preview-window=right") {
			skip = false
			// Write new fzf opts
			newLines = append(newLines, fmt.Sprintf(`$env:FZF_DEFAULT_OPTS="%s"`, t.Fzf.Opts))
			continue
		}
		if !skip {
			newLines = append(newLines, line)
		}
	}

	return os.WriteFile(profilePath, []byte(strings.Join(newLines, "\n")), 0o644)
}
