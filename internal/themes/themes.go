//go:embed data/*.json
var themeFiles embed.FS

func List() []string {
	entries, _ := themeFiles.ReadDir("data")
	names := make([]string, 0, len(entries))
	for _, e := range entries {
		name := strings.TrimSuffix(e.Name(), ".json")
		names = append(names, name)
	}
	return names
}

func Apply(name string) error {
	// Read theme JSON
	data, err := themeFiles.ReadFile(fmt.Sprintf("data/%s.json", name))
	if err != nil {
		return fmt.Errorf("theme file not found: %w", err)
	}

	// Parse theme
	theme, err := parseTheme(data)
	if err != nil {
		return fmt.Errorf("invalid theme file: %w", err)
	}

	// Apply WezTerm config
	if err := writeWezTerm(theme); err != nil {
		return fmt.Errorf("failed to apply WezTerm theme: %w", err)
	}
	fmt.Println("  ✓ WezTerm")

	// Apply Starship config
	if err := writeStarship(theme); err != nil {
		return fmt.Errorf("failed to apply Starship theme: %w", err)
	}
	fmt.Println("  ✓ Starship")

	// Apply FZF env in profile
	if err := writeFzfColors(theme); err != nil {
		return fmt.Errorf("failed to apply fzf theme: %w", err)
	}
	fmt.Println("  ✓ fzf")

	// Save current theme
	if err := saveCurrent(name); err != nil {
		fmt.Printf("  ⚠  Could not save current theme name: %v\n", err)
	}

	fmt.Printf("\n✓ Theme '%s' applied. Restart WezTerm to see changes.\n", name)
	return nil
}

func saveCurrent(name string) error {
	dir := filepath.Join(os.Getenv("USERPROFILE"), ".winshell")
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return err
	}
	return os.WriteFile(filepath.Join(dir, "current_theme"), []byte(name), 0o644)
}

func GetCurrent() string {
	path := filepath.Join(os.Getenv("USERPROFILE"), ".winshell", "current_theme")
	data, err := os.ReadFile(path)
	if err != nil {
		return "none"
	}
	return string(data)
}

